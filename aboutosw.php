<h1>About Opensim Web</h1>
<p>
This all got started as a side project for Christopher Strachan aka Chrisx84 as a past time and to give back to the opensim project.<br>
Back in early 2013 when Chris tried to start his own grid he noticed the lack of CMS website for opensim and what was available was either out of date or a complete code mess. So he started modifing his copy of the Quadodo Login Script that he has grown to love over the years using it to make a number of failed websites.<br>
But sadly even his own grid was a failure and was shut down but that never stopped Chris from coding stuff in php and lsl.<br>
In late 2013 and into early 2014 he starting thinking and planning on a brand new content management system exclusively for opensim but he was stuck on a name for it so he posted help for a name for his new cms idea on a G+ community called <a href='https://plus.google.com/u/0/communities/116284417302234467612'>Opensim Virtual</a> where just a short few minutes after posting the idea and asking for help for a name for it, Talla Adam gave a suggestion for a name for his cms idea and he immediately went with that name and started working on his idea using his old fav QLS code as referrences for the more complex login, register and database scripting.<br>
Out of all that hard work and days sitting at his computer listening to Digitally Imported Hardstyle radio channel, Opensimweb was born.
</p>
